double FilterCoef(double X)
{
    if (X > 1.0)  return 0.0;
    if (X <= 0.0) return 1.0;
    return cos(PI / 2 * X);
}

bool LinkLame::InitPolyphaseFilterbank(double ResultFilter[SUBBAND_COUNT], int SampleRate)
{
	IFALTERFUNCRET(InitPolyphaseFilterbank, (ResultFilter, SampleRate));

	const int HighpassFrequency = 0, LowpassFrequency = 17000,
		HighpassWidth = -1, LowpassWidth = -1;

	double HighpassLeft, HighpassRight,
		LowpassLeft, LowpassRight;

    /* apply user driven high pass filter */
    if ( HighpassFrequency > 0 ) 
	{
        HighpassLeft = 2. * HighpassFrequency;

        if (HighpassWidth >= 0)
            HighpassRight = 2. * (HighpassFrequency + HighpassWidth);
        else            /* 0% above on default */
            HighpassRight = (1 + 0.00) * 2. * HighpassFrequency;

        HighpassLeft  /= SampleRate; //cfg->samplerate_out;
        HighpassRight /= SampleRate; //cfg->samplerate_out;
    }
    else 
	{
        HighpassLeft = 0;
        HighpassRight = 0;
    }
    /* apply user driven low pass filter */
    LowpassLeft = 0;
    LowpassRight = 0;
    if ( LowpassFrequency > 0 && LowpassFrequency < SampleRate/2 )
	{
		LowpassRight = 2. * LowpassFrequency;
        if (LowpassWidth >= 0) {
            LowpassLeft = 2. * (LowpassFrequency - LowpassWidth);
            if (LowpassLeft < 0) /* has to be >= 0 */
                LowpassRight = 0;
        }
        else           /* 0% below on default */
            LowpassLeft = (1 - 0.00) * 2. * LowpassFrequency;
        
        LowpassLeft  /= SampleRate;
        LowpassRight /= SampleRate;
    }

	/***************************************************************/
    /* compute info needed for polyphase filter (filter type==0, default) */
    /***************************************************************/

    int band, maxband, minband;
    double freq;
    int lowpass_band = 32;
    int highpass_band = -1;

    if (LowpassLeft > 0) {
        minband = 999;
        for (band = 0; band <= 31; band++) {
            freq = band / 31.0;
            /* this band and above will be zeroed: */
            if (freq >= LowpassRight) {
                lowpass_band = min(lowpass_band, band);
            }
            if (LowpassLeft < freq && freq < LowpassRight) {
                minband = min(minband, band);
            }
        }

        /* compute the *actual* transition band implemented by
         * the polyphase filter */
        if (minband == 999) {
            LowpassLeft = (lowpass_band - .75) / 31.0;
        }
        else {
            LowpassLeft = (minband - .75) / 31.0;
        }
        LowpassRight = lowpass_band / 31.0;
    }

    /* make sure highpass filter is within 90% of what the effective
     * highpass frequency will be */
    if (HighpassRight > 0) {
        if (HighpassRight < .9 * (.75 / 31.0)) {
            HighpassLeft = 0;
            HighpassRight = 0;
#ifndef MGINEMP3LINK_EXPORTS
			Log::WriteLog(L"%s: Warning: highpass filter disabled. highpass frequency too small.", __FUNCTIONW__);
#endif
        }
    }

    if (HighpassRight > 0) 
	{
        maxband = -1;
        for (band = 0; band <= 31; band++) {
            freq = band / 31.0;
            /* this band and below will be zereod */
            if (freq <= HighpassLeft) {
                highpass_band = max(highpass_band, band);
            }
            if (HighpassLeft < freq && freq < HighpassRight) {
                maxband = max(maxband, band);
            }
        }
        /* compute the *actual* transition band implemented by
         * the polyphase filter */
        HighpassLeft = highpass_band / 31.0;
        if (maxband == -1) 
            HighpassRight = (highpass_band + .75) / 31.0;
        else HighpassRight = (maxband + .75) / 31.0;
    }

    for (band = 0; band < 32; band++) 
	{
        double fc1, fc2;
        freq = band / 31.0f;
        if (HighpassRight > HighpassLeft)
            fc1 = FilterCoef((HighpassRight - freq) / (HighpassRight - HighpassLeft + 1e-20));
        else
            fc1 = 1.0f;
        if (LowpassRight > LowpassLeft)
            fc2 = FilterCoef((freq - LowpassLeft)  / (LowpassRight - LowpassLeft + 1e-20));
        else
            fc2 = 1.0f;
        ResultFilter[band] = fc1 * fc2;
    }

	return true;
}

